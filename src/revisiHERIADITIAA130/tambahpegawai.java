    private void TampilData(){
        try{
            String sql = "Select *from user where level=2"; //menampilkan seluruh data dari tabel buku
            stt = con.createStatement(); //menampilakn data dengan mengkoneksikan ke database
            rss = stt.executeQuery(sql); //menampung data dengan mengeksekusi seiap query
            while(rss.next()){
                Object[] o = new Object[2];
                o[0] = rss.getString("username");
                o[1] = rss.getString("password"); //menampung data pada penulis
                model.addRow(o); //menambah baris
            }
        }catch(SQLException e){
            System.out.println(e.getMessage());
        }
    }
    
    private boolean validasi(String username, String password){ //
        try{ //mengeksekusi program yang berada di dalam try
            stt=con.createStatement();
            String sql = "Select * from user where username = '"+username+"' and password='"+password+"';"; //menampilkan semua data pada tabel buku dimana pencarian berdasarkan judul
            rss=stt.executeQuery(sql);
            if (rss.next())
                return true;
            else
                return false;
        }
            catch (SQLException e){ //catch untuk menentukan jenis exception yang ingin ditangani 
                System.out.println(e.getMessage());
                return false;
            }
    }
    
    private boolean ubahdata (String id, String username, String password){
        try{
            String sql = "Update user set username='"+username+"', password='"+password+"' where id_user="+id+";"; //data yang bisa diubah judul, penulis, dan harga
            stt=con.createStatement();
            stt.executeUpdate(sql);
            return true;
        }catch(SQLException e){
            System.out.println(e.getMessage());
            return false;
        }
    }
    
    private boolean hapusdata (String nama){
        try{
            String sql = "delete from user where username='"+nama+"';";
            stt=con.createStatement();
            stt.executeUpdate(sql);
            return true;
        }catch(SQLException e){
            System.out.println(e.getMessage());
            return false;
        }
    }
    
    private void TambahData(String username, String password){ //method tambah data
        try{ //mengeksekusi program yang berada di dalam try
            String sql = "insert into user values (NULL,'"+username+"','"+password+"','2')"; //menginput data pada judul penulis dan harga
            stt = con.createStatement();
            stt.executeUpdate(sql);
            model.addRow(new Object[]{username,password});
        }catch(SQLException e){ //catch untuk menentukan jenis exception yang ingin ditangani
            System.out.println(e.getMessage());
        }
    }
    
    
